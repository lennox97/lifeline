package com.ahope.liniavietii;

public class Quote {
    public String title;
    public String year;
    public String quote;
    public String chapter;
    public String vers;

    public Quote() {

    }

    public Quote(String title, String year, String quote, String chapter, String vers) {
        this.title = title;
        this.year = year;
        this.quote = quote;
        this.chapter = chapter;
        this.vers = vers;
    }
}
